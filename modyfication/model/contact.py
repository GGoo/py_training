class Contact:
    def __init__(self, name="None", middlename="None", lastname="None", nickname="None", company="None", address="None", homephone="None",
                 mobile="None", mail="None", birthyear="None"):
        self.name = name;
        self.middlename = middlename;
        self.lastname = lastname;
        self.nickname = nickname;
        self.company = company;
        self.address = address;
        self.homephone = homephone;
        self.mobile = mobile;
        self.mail = mail;
        self.birthyear = birthyear;
